Definir el concepto de nivel vinculado a programación web.
@endsection

@section('contenido')
Nivel hace referencia a la forma en la cual las capas de distribuyen de forma física. 
El nivel es la parte en la cual se dividen las diferentes partes de un sistema de software. Los niveles nos ayudan ya que si se ve afectado algún nivel solo se realizarán modificaciones en este y los demás niveles no se verán afectados. Cada nivel estará separado del resto de los niveles solo bastará con conocer la API que existe entre cada nivel 
