@section('titulotema')
Definir el concepto de comunicación orientada a conexión e interfaz de programación de aplicaciones (API).
@endsection

@section('contenido')
Comunicación orientada a conexión: Es un tipo de modelo de comunicación de los datos. Este modelo es requerido por protocolos de red de computadoras para hacer más eficaz el envió de paquetes, haciendo que estos no se pierdan, para llevar esto a cabo se necesita una sobrecarga en la configuración de la solicitud de la conexión.
API: Interfaz de programación de aplicaciones. Permite la comunicación entre aplicaciones de software. Este provee de información a un tercero. 
@endsection

