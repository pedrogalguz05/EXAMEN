@section('contenido')
Los servicios y funcionalidad en nube que incluyen computación y almacenamiento se combinan para permitir Software como un servicio (SaaS), Plataforma como un servicio (PaaS) e Infraestructura como un servicio (IaaS) en soluciones de nube pública y privada. Estas capacidades se pueden ofrecer como un servicio, como un producto o como una solución incluida, también conocida como TI como un servicio (ITaaS). Los servicios en nube son reunidos en todas las infraestructuras públicas y privadas para crear nubes híbridas para satisfacer sus necesidades y requerimientos específicos.
@endsection

