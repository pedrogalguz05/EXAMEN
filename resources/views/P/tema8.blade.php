@section('titulotema')
Definir los conceptos de Infrastructure as a Service (IAAS), Platform as a Service (PAAS), Software as a Service (SAAS).
@endsection

@section('contenido')
IAAS: tipo de modelo de servicio de Cloud Computing en el que los recursos de computación se alojan en la nube
PAAS: desarrollo e implementación completo en la nube, con recursos que permiten entregar todo como aplicaciones sencillas basadas en la nube hasta aplicaciones empresariales sofisticadas habilitadas para la nube
SAAS: modelo de distribución de software en el que un proveedor de la nube aloja aplicaciones y las pone a disposición de los usuarios finales a través de Internet.
@endsection

