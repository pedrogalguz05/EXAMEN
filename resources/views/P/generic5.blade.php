							<h1>SISTEMAS CON ARQUITECTURA CLIENTE/SERVIDOR.</h1>
							<span class="image main"><img src="images/foto06.jpeg" alt="" /></span>
							<p>Hace referencia a un determinado cliente el cual ejecuta diversas peticiones a otro programa el cual a su vez se considera como servidor, este será encargado de proporcionar una respuesta.</p>
                            <P>COMPONENTES DE LA ARQUITECTURA:<br>
                                - El cliente: Hace referencia a un determinado demandante de servicios, este cliente puede ser algún ordenador o una aplicación del área informática, la cual requiera de información proveniente de la red para funcionar de manera adecuada.<br>
                                - El servidor: Es un proveedor de servicios, los cuales a su vez pueden ser ordenadores o aplicaciones del área informática, las cuales pueden enviar información a los demás agentes que se encuentran conectados a la red.<br>
                                - La red: La red se basa en un conjunto de clientes, servidores y diversos datos que se encuentran enlazados de una manera física o no física.<br>
                                - El servicio: Un servicio se puede definir como un conjunto de información específica, que tiene como propósito proporcionar una respuesta a las necesidades de un cliente en específico.<br>
                                - El protocolo: El protocolo se basa en un grupo de normas, reglas y diversos pasos debidamente establecidos, estos se pueden reflejar de manera clara y concreta acerca del flujo de información en una determinada red debidamente estructurada.<br>
                                - Base de datos: Esta base se define como diversos bancos de información debidamente ordenada, categorizada y clasificada, las cuales, forman parte de la red, las cuales son distintos sitios de almacenaje para la implementación de los servidores. </P>
                            <p>TIPOS DE ARQUITECTURAS:<br>
                                - La arquitectura de dos capas: Puede ser implementada para los diversos sistemas cliente servidor, en los que los clientes pueden solicitar ciertos recursos y el servidor que corresponde, deberá responder de manera directa ante la solicitud realizada con sus propios recursos.<br>
                                - La arquitectura de tres capas: La capa del medio se le conoce como software intermedio, este tiene como objetivo el proporcionar todos los recursos que son solicitados, pero que requieren de otro servicio para realizarlo.<br>
                                - Arquitectura N capas: En este tipo de arquitectura, los servidores dos y tres ya han ejecutado una determinada tarea, por lo tanto, el servidor web podrá utilizar los diversos servicios de otros servidores. De esta manera, se podrá proporcionar su propio servicio. </p>
